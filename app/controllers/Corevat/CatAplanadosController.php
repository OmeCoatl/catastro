<?php

class corevat_CatAplanadosController extends \BaseController {

	protected $catalogo;

	/**
	 * Instancia del catalogo de aplanados [COREVAT]
	 * @var Catalogo de aplanados [COREVAT]
	 */
	public function __construct(CatAplanados $catalogo) {
		$this->catalogo = $catalogo;
	}

	/**
	 * Display a listing of the resource.
	 *
	 * @return Response
	 */
	public function index($format = 'html') {
		$title = 'COREVAT';
		$title_section = 'Catálogo de Aplanados';
		$titleGrid = 'Catálogo de Aplanados';
		$row = $this->catalogo;
		$rows = CatAplanados::orderBy('aplanado', 'asc')->get();
		return ($format == 'json') ? $rows : View::make('CorevatCatalogos.CatAplanados.index', compact('title', 'title_section', 'row', 'rows', 'titleGrid'));
	}

	/**
	 * Show the form for creating a new resource.
	 *
	 * @return Response
	 */
	public function create() {
		$title = 'COREVAT';
		$title_section = 'Catálogo de Aplanados';
		$titleGrid = 'Catálogo Aplanados';
		$row = $this->catalogo;
		$row->status = 1;
		$rows = CatAplanados::orderBy('aplanado', 'asc')->get();
		return View::make('corevatcatalogos.CatAplanados.create', compact('title', 'title_section', 'row', 'rows', 'titleGrid'));
	}

	/**
	 * Store a newly created resource in storage.
	 *
	 * @return Response
	 */
	public function store($format = 'html') {
		$id=Input::get('id');
		$inputs = Input::All();
		$rules = array(
			'aplanado' => 'required',
		);
		$messages = array(
			'required' => 'El campo es requerido',
		);
		$validate = Validator::make($inputs, $rules, $messages);
		if ($validate->fails()) {
			return Redirect::back()->withInput()->withErrors($validate);
		}  else {
			$row = new CatAplanados;
			$row->aplanado = $inputs["aplanado"];
			$row->status = isset($inputs["status"]) ? $inputs["status"] : 0;
			$row->idemp = 1;
			$row->ip = $_SERVER['REMOTE_ADDR'];
			$row->host = isset($_SERVER['HTTP_CLIENT_IP']) ? $_SERVER['HTTP_CLIENT_IP'] : '';
			$row->creado_por = 1;
			$row->creado_el = date('Y-m-d H:i:s');
			$row->save();
			return Redirect::to('corevat/CatAplanados/create')->with('success', '¡Se ha creado correctamente el registro!');
		}
	}

	/**
	 * Display the specified resource.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function show($id) {
		//
	}

	/**
	 * Show the form for editing the specified resource.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function edit($id) {
		$title = 'COREVAT';
		$title_section = 'Catálogo Aplanados';
		$titleGrid = 'Catálogo Aplanados';
		$row = CatAplanados::find($id);
		$rows = CatAplanados::orderBy('aplanado', 'asc')->get();
		$id = $row->idaplanado;
		return View::make('corevatcatalogos.CatAplanados.edit', compact('title', 'title_section', 'row', 'rows', 'id', 'titleGrid'));
	}

	/**
	 * Update the specified resource in storage.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function update($id, $format = 'html') {
		$inputs = Input::All();
		$row = CatAplanados::find($id);
		$rules = array(
			'aplanado' => 'required',
		);
		$messages = array(
			'required' => 'El campo es requerido',
		);
		$validate = Validator::make($inputs, $rules, $messages);
		if ($validate->fails()) {
			return Redirect::back()->withInput()->withErrors($validate);
		} else {
			$row->aplanado = $inputs["aplanado"];
			$row->status= isset($inputs["status"]) ? $inputs["status"] : 0;
			$row->modi_por = 1;
			$row->modi_el = date('Y-m-d H:i:s');
			$row->save();
			return Redirect::to('corevat/CatAplanados/' . $id . '/edit')->with('success', '¡La modificación se efectuo correctamente!');
		}
	}

	/**
	 * Remove the specified resource from storage.
	 *
	 * @param  int  $id
	 * @return Response
	 */
	public function destroy($id = null) {
        $row = CatAplanados::findOrFail($id);
        $row->delete($id);
        return Redirect::to('corevat/CatAplanados')->with('success', '¡La eliminación se efectuo correctamente!');
	}

}
